package bean;

public class User {
    private String userName;
    private String passWord;

    public User(){}
    public String getUserName() {
        return userName;
    }
    public void setUserName(String name) {
        this.userName = name;
    }
    public String getPassWord() {
        return passWord;
    }
    public void setPassWord(String url) {
        this.passWord = url;
    }
}
